    public function findByParticipatingUser(User $user)
    {
        $qb = $this->createQueryBuilder('e')
            ->innerJoin('e.participants', 'u')  // Join on the participants relationship
            ->where('u.id = :userId')
            ->setParameter('userId', $user->getId());

        return $qb->getQuery()->getResult();
